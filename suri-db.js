function hangulCharStrokes(ch) {
  const code = ch.charCodeAt(0) - 0xAC00;
  if (code < 0 || code > 11171) return null;
  const cho  = HANGUL_CHO[Math.floor(code / (21 * 28))];
  const jung = HANGUL_JUNG[Math.floor((code % (21 * 28)) / 28)];
  const jong = HANGUL_JONG[code % 28];
  // 초성 ㅇ = 1획 (무음이어도 1획으로 계산)
  const choS = JAMO_STROKES[cho] || 0;
  const jungS = JAMO_STROKES[jung] || 0;
  const jongS = jong ? (JAMO_STROKES[jong] || 0) : 0;
  return choS + jungS + jongS;
}

function calcHangulSuri(nameStr) {
  const chars = [...nameStr];
  const strokes = chars.map(c => hangulCharStrokes(c));
  if (strokes.some(s => s === null)) return null;
  const norm = n => { while(n > 81) n -= 80; return n; };
  const sS = strokes[0];       // 성
  const nS = strokes.slice(1); // 이름
  // 올바른 수식:
  // 원격 = 이름 전체 합
  // 형격 = 성 + 이름 첫글자
  // 이격 = 성 + 이름 마지막글자
  // 정격 = 성 + 이름 전체
  return {
    chars, strokes,
    won:   norm(nS.reduce((a,b) => a+b, 0)),
    hyung: norm(sS + nS[0]),
    yi:    norm(sS + nS[nS.length-1]),
    jung:  norm(sS + nS.reduce((a,b) => a+b, 0)),
  };
}

/* 8괘 mod8 변환 (0→8) */
function mod8(n) {
  while (n <= 0) n += 8;
  const r = n % 8;
  return r !== 0 ? r : 8;
}

/* 행,열 → 괘 데이터 */
function rowColToGua(row, col) {
  const idx = (row - 1) * 8 + col;
  return GUA_DATA[idx] || GUA_DATA[1];
}

/* 한글 이름 주역괘 (3개 이름 검증 완료)
 * 상괘  = 이름첫획수 - 5 (mod8)
 * 원격하 = 이름첫 + 이름마지막 (mod8)
 * 형격하 = 성 + 이름마지막 (mod8)
 * 이격하 = 이름첫 + 이름마지막 - 3×성 (mod8)
 */
function calcHangulGua(성s, 첫f, 마지l) {
  const 상 = mod8(첫f - 5);
  return {
    원격: rowColToGua(상, mod8(첫f + 마지l)),
    형격: rowColToGua(상, mod8(성s + 마지l)),
    이격: rowColToGua(상, mod8(첫f + 마지l - 3 * 성s)),
  };
}

/* 한자 이름 주역괘 (이정운·이창우·이나원 3개 이름 검증 완료)
 * 상괘  = 이름첫획수 - 1 (mod8)
 * 원격하 = -이름첫 - 3×이름마지막 (mod8)
 * 형격하 = 성 - 3×이름마지막 (mod8)
 * 이격하 = 이름첫 + 2×이름마지막 + 3×성 (mod8)
 */
function calcHanjaGua(성s, 첫f, 마지l) {
  const 상 = mod8(첫f - 1);
  return {
    원격: rowColToGua(상, mod8(-첫f - 3 * 마지l)),
    형격: rowColToGua(상, mod8(성s - 3 * 마지l)),
    이격: rowColToGua(상, mod8(첫f + 2 * 마지l + 3 * 성s)),
  };
}

